const sub = (a, b) => {
  const c = [];

  for (let i = 0; i < a.length; i++) {
    if (a[i].length === b[i].length) {
      c[i] = a[i].map((n, index) => n - b[i][index]);
    } else {
      return false;
    }
  }
  return c;
};

