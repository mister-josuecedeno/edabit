const holeySort = (numbers) => {
  const holes = {
    0: 1,
    4: 1,
    6: 1,
    8: 2,
    9: 1,
  };

  const totalHoles = (num) => {
    let total = `${num}`
      .split('')
      .map((n) => holes[n] || 0)
      .reduce((a, b) => a + b, 0);
    return total;
  };

  numbers.sort((a, b) => {
    if (totalHoles(a) === totalHoles(b)) return a;

    return totalHoles(a) - totalHoles(b);
  });

  return numbers;
};

